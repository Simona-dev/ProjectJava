package Lab2.AnimalRescue;

public class Girl {

    private String name;
    private float money;
    private int age;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setMoney(float money) {
        this.money = money;
    }
    public float getMoney() {
        return money;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return (int) age;
    }
}
